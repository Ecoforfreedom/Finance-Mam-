def ensure_style(doc):
    doc.styles['Normal'].font.name = 'Microsoft YaHei'
    doc.styles['Normal'].font.size = Pt(10.5)

def write_docx(rel, title, meta, sections):
    path = ROOT / rel
    path.parent.mkdir(parents=True, exist_ok=True)
    doc = Document(); ensure_style(doc); doc.add_heading(title, 1)
    t = doc.add_table(rows=0, cols=2)
    for k,v in meta.items():
        cells=t.add_row().cells; cells[0].text=str(k); cells[1].text=str(v)
    for h, body in sections:
        doc.add_heading(h, 2)
        for para in str(body).split('\n'):
            if para.strip(): doc.add_paragraph(para.strip())
    doc.save(path)
    return path

def write_md(rel, text):
    path = ROOT/rel; path.parent.mkdir(parents=True, exist_ok=True); path.write_text(text, encoding='utf-8'); return path

def write_xlsx(rel, sheets):
    path=ROOT/rel; path.parent.mkdir(parents=True, exist_ok=True)
    wb=Workbook(); wb.remove(wb.active)
    for sn, rows in sheets.items():
        ws=wb.create_sheet(sn[:31]); heads=list(rows[0].keys()) if rows else ['说明']; ws.append(heads)
        for r in rows: ws.append([r.get(h,'') for h in heads])
        ws.freeze_panes='A2'; ws.auto_filter.ref=ws.dimensions
        for c in ws[1]: c.font=Font(bold=True,color='FFFFFF'); c.fill=PatternFill('solid', fgColor='1F4E78'); c.alignment=Alignment(horizontal='center')
        for col in ws.columns:
            ws.column_dimensions[col[0].column_letter].width=min(max(max(len(str(c.value or '')) for c in col)+2, 12), 55)
    wb.save(path); return path

