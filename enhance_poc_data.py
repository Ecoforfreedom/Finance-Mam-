def infer_doc(path):
    rel=str(path.relative_to(ROOT)).replace('\\','/')
    pid='ALL'; pname='通用'
    for p in projects:
        if p['id'] in path.name:
            pid=p['id']; pname=p['name']; break
    typ=path.parent.name
    for t in ['季度投后报告','GP季度投后报告','投资协议','股东协议','补充协议','董事会会议纪要','财务报表','资金使用台账','企业访谈纪要','GP沟通邮件','项目问询回复','模拟新闻报道','企业公众号文章']:
        if t in path.name: typ=t
    per='不适用'
    for q in Q:
        if q in rel: per=q
    m=re.search(r'(20\d{6})', path.name); dt='2026-07-30'
    if m: dt=f"{m.group(1)[:4]}-{m.group(1)[4:6]}-{m.group(1)[6:8]}"
    return {"文件编号":f"D{infer_doc.counter:04d}","项目编号":pid,"项目名称":pname,"材料类型":typ,"报告期":per,"材料日期":dt,"文件版本":"V2.0" if 'V2.0' in path.name else "V1.0","信息来源":"POC模拟材料","私域或外部":"外部" if '07_外部公开信息' in rel else "私域","文件路径":rel}
